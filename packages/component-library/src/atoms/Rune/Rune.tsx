/**
 *
 * @param {string} mod accepts 4 possible values. | frontend | data | backend | mobile
 * @param {string} size accepts 3 possible values. | sm -> 20*20px | md 30*60px | lg -> 50*80px | by default is sm
 * @param {boolean} progressbar  activate progress bar | by default is false
 * @param {number} min range minimum | by default is 0
 * @param {number} min range maximum | by default is 0
 * @param {number} points  points of user  | by default is 0
 * @returns {jsx} jsx component
 */

export const Rune = ({
  mod = 'nuwe',
  size = 'sm',
  progressbar = false,
  min = 0,
  max = 0,
  points = 0
}) => {
  const rune = {
    data: <RuneData size={size} progressbar={progressbar} min={min} max={max} points={points} />,
    frontend: (
      <RuneFrontend size={size} progressbar={progressbar} min={min} max={max} points={points} />
    ),
    backend: (
      <RuneBackend size={size} progressbar={progressbar} min={min} max={max} points={points} />
    ),
    mobile: (
      <RuneMobile size={size} progressbar={progressbar} min={min} max={max} points={points} />
    ),
    nuwe: <RuneNuwe size={size} progressbar={progressbar} min={min} max={max} points={points} />
  }

  return rune[mod] || rune.nuwe
}
